# Basic arithmetic operations

print(3 + 2)  # Addition
print(3 - 2)  # Subtraction
print(3 * 2)  # Multiplication
print(3 / 2)  # Floating point division
print(3 // 2)  # Integer/floor division
print(3 % 2)  # Modulo (remainder)

# Assigning values to variables
x = 10
y = 20

# Alternatively assign multiple variables in one line
x, y = 10, 20

# Operations that include a floating point variable will also result in a float
print(x + 1.0)

# Complex numbers are also supported
c1 = 2 + 2j
c2 = 4 - 8j
print(c1 + c2)
